// Déclaration et assignation des variables
var taille = 30;
var appelation = "Alice";
var isStudent = true;
var nothing = undefined;
// Opérations de base
taille += 5; // Ajoute 5 à age
appelation = appelation.toUpperCase(); // Convertit appellation en majuscules
isStudent = !isStudent; // Inverse la valeur de isStudent
// Affichage des résultats
console.log("taille après ajout de 5:", taille);
console.log("Nom en majuscules:", appelation);
console.log("Est-ce un étudiant maintenant ?", isStudent);
console.log("Valeur de la variable undefined:", nothing);
